public class CalculadoraBasica {
    public static int soma(int val1soma, int val2soma){
        return val1soma + val2soma;
    }
    public static int subtracao(int val1subtracao, int val2subtracao){
        return val1subtracao - val2subtracao;
    }
    public static int multiplicacao(int val1mult, int val2mult){
        return val1mult * val2mult;
    }
    public static Double divisao (Double val1div, Double val2div){
        return val1div / val2div;
    }
    public static Double Porcentagem (Double valpor, Double porcentagem){
        return porcentagem / 100 * valpor;
    }
    
}
